    private void reponerStockBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reponerStockBtnActionPerformed
        try {
            if (codProductoSeleccionado != -1) {
                int nuevoStock = Integer.parseInt(nuevoStockField.getText());
                pc.editarStock(codProductoSeleccionado, nuevoStock, stockSeleccionado);
                
                alistarTabla();
                this.codProductoSeleccionado = -1;
                JOptionPane.showMessageDialog(this, "¬°STOCK DE PRODUCTO EDITADO EXITOSAMENTE!");
            } else{
                JOptionPane.showMessageDialog(this, "Por favor selecciona un producto de la tabla.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El stock debe ser un n√∫mero v√°lido", "Error de formato", JOptionPane.ERROR_MESSAGE);
        } catch (StockInsuficienteException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_reponerStockBtnActionPerformed


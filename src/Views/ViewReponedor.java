    private void alistarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.setColumnIdentifiers(new Object[]{"Codigo", "Nombre", "Precio", "Stock", "Tipo","Proveedor", "Caducidad", "DuracionAlmacen"});
        
        for (int i = 0; i < pc.getProductos(3, stockBajoCheckbox.isSelected()).size(); i++) {
            Producto pro = pc.getProductos(3, stockBajoCheckbox.isSelected()).get(i);
            modelo.addRow(new Object[]{
                pro.getCodigoProducto(),
                pro.getNombreProducto(),
                pro.getPrecio(),
                pro.getStock(),
                pro.getClass().getSimpleName().equals("ProductoPerecedero")? "Perecedero":"NoPerecedero",
                pro.getIdProveedor(),
                pro instanceof ProductoPerecedero? ((ProductoPerecedero)pro).getFechaCaducidad() : "",
                pro instanceof ProductoNoPerecedero? ((ProductoNoPerecedero)pro).getDuracionAlmacen() : ""
            });
        }
        tablaProductos.setModel(modelo);
    }
    private void configurarSeleccionTablaProductos() {
        tablaProductos.getSelectionModel().addListSelectionListener(event -> {
            if (!event.getValueIsAdjusting()) {
                int filaSeleccionada = tablaProductos.getSelectedRow();

                if (filaSeleccionada != -1) {
                    Object codProd = tablaProductos.getValueAt(filaSeleccionada, 0);
                    Object stockProd = tablaProductos.getValueAt(filaSeleccionada, 3);
                    this.codProductoSeleccionado = Integer.parseInt(codProd.toString());
                    this.stockSeleccionado = Integer.parseInt(stockProd.toString());
                }
            }
        });
    }
